package loop;

public class While1_1 {

    public static void main(String[] args) {
        // 반복문의 필요성
        // 같은 코드를 반복시행해야할 때
        // 반복문은 while문 for문

        int count = 0;

        count = count + 1;
        System.out.println("현재 숫자는:" + count);
        count = count + 1;
        System.out.println("현재 숫자는:" + count);
        count = count + 1;
        System.out.println("현재 숫자는:" + count);
    }

}
